    private void btnRegisterRegMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnRegisterRegMouseClicked
        // TODO add your handling code here:
        SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy");
        String DOB = sdf.format(clndrDOBReg.getDate()); 
        String AccNum = txtAccNumReg.getText();
        boolean condition = true;
        for (int i = 0; i < tblCustList.getRowCount(); i++){
            if(txtAccNumReg.getDocument().getLength() < 11){
                JOptionPane.showMessageDialog(this, "Account Number must Contain 11 Digits", "Alert" , JOptionPane.WARNING_MESSAGE);
                condition = false;
                break;
            }
            else if (AccNum.equals(tblCustList.getValueAt(i, 1).toString())){
                JOptionPane.showMessageDialog(this, "Account Number Already Taken", "Warning" , JOptionPane.ERROR_MESSAGE);
                condition = false;
                break;
            }
        }
            if(condition) {
                String customerReg[] = {txtNameReg.getText(), txtAccNumReg.getText(), txtHomeAddReg.getText(), spnrPrevMeterReg.getValue().toString(), spnrCurrMeterReg.getValue().toString(), DOB};
        
                DefaultTableModel tbl = (DefaultTableModel) tblCustList.getModel();
                tbl.addRow(customerReg);
            
                JOptionPane.showMessageDialog(this, "Customer Registered");
            
                txtNameReg.setText(null);
                txtAccNumReg.setText(null);
                txtHomeAddReg.setText(null);
                spnrPrevMeterReg.setValue(Integer.parseInt("0"));
                spnrCurrMeterReg.setValue(Integer.parseInt("0"));
                clndrDOBReg.setDate(null);
            }           
    }//GEN-LAST:event_btnRegisterRegMouseClicked

    private void btnFindMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnFindMouseClicked
        // TODO add your handling code here:
        DefaultTableModel tbl = (DefaultTableModel) tblCustList.getModel();
        String searchData = txtAccNumSearch.getText();
        
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<DefaultTableModel> (tbl);
        tblCustList.setRowSorter(tr);
        tr.setRowFilter(RowFilter.regexFilter(searchData));
    }//GEN-LAST:event_btnFindMouseClicked

