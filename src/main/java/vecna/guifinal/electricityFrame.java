    private void btnFindEditMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnFindEditMouseClicked
        // TODO add your handling code here:
        String accNum = txtaccNumFind.getText();
        for (int i = 0; i < tblCustList.getRowCount(); i++){
            if(accNum.equals(tblCustList.getValueAt(i, 1).toString())){
                txtNameEdit.setText(tblCustList.getValueAt(i, 0).toString());
                txtAccNumEdit.setText(txtaccNumFind.getText());
                txtHomeAddEdit.setText(tblCustList.getValueAt(i, 2).toString());
                spnrPrevMeterEdit.setValue(Integer.parseInt(tblCustList.getValueAt(i,3).toString()));
                spnrCurrMeterEdit.setValue(Integer.parseInt(tblCustList.getValueAt(i,3).toString()));
                try {
                    Date date = new SimpleDateFormat("dd-MM-yyyy").parse((String)tblCustList.getValueAt(i,5));
                    clndrDOBEdit.setDate(date);
                } catch (ParseException e) {
                    // TODO Auto-generated catch block
                    e.printStackTrace();
                }
            }
        }
        if(txtNameEdit.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Enter Correct Account Number Details");
        }
    }//GEN-LAST:event_btnFindEditMouseClicked

    private void btnUpdateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnUpdateMouseClicked
        // TODO add your handling code here:
        DefaultTableModel tbl = (DefaultTableModel) tblCustList.getModel();
        
        for(int i= 0; i< tblCustList.getRowCount(); i++){
            if(txtAccNumEdit.getText().equals(tblCustList.getValueAt(i, 1).toString())){
                tbl.setValueAt(txtNameEdit.getText(), i, 0);
                tbl.setValueAt(txtHomeAddEdit.getText(), i, 2);
                tbl.setValueAt(spnrPrevMeterEdit.getValue(), i, 3);
                tbl.setValueAt(spnrCurrMeterEdit.getValue(), i, 4);
                
                JOptionPane.showMessageDialog(this, "Account Updated");
                
                txtNameEdit.setText(null);
                txtAccNumEdit.setText(null);
                txtaccNumFind.setText(null);
                txtHomeAddEdit.setText(null);
                spnrPrevMeterEdit.setValue(Integer.parseInt("0"));
                spnrCurrMeterEdit.setValue(Integer.parseInt("0"));
                clndrDOBEdit.setDate(null);
            }
        }
    }//GEN-LAST:event_btnUpdateMouseClicked

